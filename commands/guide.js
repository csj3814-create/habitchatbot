/**
 * General onboarding / help text for MessengerBot users.
 */

async function handleGuide(sender) {
    return `해빛스쿨 시작 가이드
━━━━━━━━━━

해빛스쿨은 식단, 운동, 수면, 마음 기록을 쌓고
해빛코치와 함께 흐름을 점검하는 건강 습관 앱이에요.

시작 순서
1. 해빛스쿨 앱 로그인
2. 앱 프로필에서 연결 코드 생성
3. 이 방에서 !등록 코드 입력

주요 명령
!오늘 - 오늘 전체 기록 요약
!내습관 - 내 기록 보기
!주간 - 주간 리포트 보기
!식단 / !운동 / !마음 - 영역별 AI 코칭
!내코드 - 친구 코드 확인
!친구 코드 - 친구 요청 보내기
!공유 - 최신 인증 카드를 이미지로 공유하기

${sender}님 기록을 바탕으로 해빛코치가 더 맞춤형으로 도와드릴게요.`;
}

async function handleApp() {
    return `해빛스쿨 앱 사용 안내
━━━━━━━━━━

1. 앱 로그인 후 식단, 운동, 수면, 마음 기록을 남겨 주세요.
2. 프로필에서 연결 코드를 만든 뒤 !등록 코드로 해빛코치와 연결해 주세요.
3. 연결 후에는 !내습관, !주간, !공유 같은 명령을 바로 쓸 수 있어요.

친구 기능도 앱과 연결돼 있어요.
- !내코드 : 내 친구 코드 확인
- !친구 코드 : 친구 요청 보내기
- 친구 수락은 앱에서 진행

궁금한 점은 이 방에서 편하게 물어봐 주세요.`;
}

module.exports = { handleGuide, handleApp };

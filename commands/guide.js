/**
 * General onboarding / help text for chatbot users.
 */

async function handleGuide(sender) {
    return `해빛스쿨 시작 가이드
────────

해빛스쿨은 식단, 운동, 수면, 마음 기록을 남기고
해빛코치는 그 기록을 바탕으로 대화를 이어가는 건강 코치 챗봇이에요.

가장 먼저 할 일
1. 해빛스쿨 앱에 로그인
2. 카카오 해빛코치 1:1 채팅에서 !연결 입력
3. 챗봇이 보내는 버튼을 눌러 앱에서 연결 완료

주요 명령
!오늘 - 오늘 기록 요약
!내습관 - 내 기록 보기
!주간 - 주간 리포트 보기
!식단 / !운동 / !마음 - 영역별 AI 코칭
!연결 - 앱 계정 연결 마무리
!내코드 - 내 초대 링크와 친구 코드 확인
!친구 코드 - 코드로 친구 요청 보내기
!공유 - 최신 인증 카드를 이미지로 공유하기

${sender}님의 기록을 참고해서 해빛코치가 더 맞춤형으로 도와드릴게요.`;
}

async function handleApp() {
    return `해빛스쿨 앱 사용 안내
────────

1. 앱에서 식단, 운동, 수면, 마음 기록을 남겨 주세요.
2. 카카오 해빛코치 1:1 채팅에서 !연결 을 입력해 주세요.
3. 챗봇이 보내는 버튼을 눌러 앱에서 연결을 완료해 주세요.
4. 연결 후에는 !내습관, !주간, !공유 같은 명령을 바로 사용할 수 있어요.

친구 연결은 이렇게 쓰면 쉬워요.
- !내코드 : 내 초대 링크와 친구 코드 확인
- 초대 링크 공유 : 신규 회원은 추천 가입, 기존 회원은 친구 연결
- !친구 코드 : 링크 대신 코드로 직접 친구 요청

궁금한 점은 이 방에서 편하게 물어봐 주세요.`;
}

module.exports = { handleGuide, handleApp };
